/*
	CRUD  STORED PROC FOR ASSESSMENT CRITERIA TABLE 
*/

USE FACULTYDB;
GO


CREATE PROC sp_InsertAssignmentCriteria
				@ASSIGNMENTID INT ,
				@CRITERIANAME NVARCHAR(100),
				@MAXSCORE INT 
AS
BEGIN
INSERT INTO ASSESSMENTCRITERIA (AssignmentId,CriteriaName,MaxScore)
VALUES (@ASSIGNMENTID,@CRITERIANAME,@MAXSCORE);
END



CREATE PROC sp_GetCriteriaByAssignment
				@ASSIGNMENTID			INT
AS
BEGIN
		SELECT * FROM ASSESSMENTCRITERIA WHERE AssignmentId = @ASSIGNMENTID;
END
GO


CREATE PROC sp_GetCriteriaById
				@CRITERIAID		INT
AS
BEGIN
		SELECT * FROM ASSESSMENTCRITERIA WHERE CriteriaId = @CRITERIAID;
END
GO



CREATE PROC sp_UpdateCriteria
				@CRITERIAID		INT,
				@CRITERIANAME		NVARCHAR(100),
				@MAXSCORE			INT
AS
BEGIN
		UPDATE ASSESSMENTCRITERIA
		SET		CriteriaName = @CRITERIANAME,
				MaxScore = @MAXSCORE
		WHERE CriteriaId = @CRITERIAID;
END
GO




CREATE PROC sp_DeleteCriteria
				@CRITERIAID		INT
AS
BEGIN
		DELETE FROM ASSESSMENTCRITERIA WHERE CriteriaId = @CRITERIAID;
END
GO











